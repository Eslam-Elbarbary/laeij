// Helper function to simulate API delay
const delay = (ms = 800) => new Promise((resolve) => setTimeout(resolve, ms));

// API Service
export const api = {
  // Get all categories
  getCategories: async () => {
    await delay(600);
    return {
      success: true,
      data: mockCategories,
      message: "Categories fetched successfully",
    };
  },

  // Get category by ID
  getCategoryById: async (id) => {
    await delay(500);
    const category = mockCategories.find((cat) => cat.id === parseInt(id));
    if (!category) {
      return {
        success: false,
        data: null,
        message: "Category not found",
      };
    }
    return {
      success: true,
      data: category,
      message: "Category fetched successfully",
    };
  },

  // Get all products
  getProducts: async (filters = {}) => {
    await delay(700);
    let filteredProducts = [...mockProducts];

    // Filter by category
    if (filters.categoryId) {
      filteredProducts = filteredProducts.filter(
        (p) => p.categoryId === parseInt(filters.categoryId)
      );
    }

    // Filter by category name
    if (filters.category) {
      filteredProducts = filteredProducts.filter(
        (p) => p.category === filters.category
      );
    }

    // Filter by tag
    if (filters.tag) {
      filteredProducts = filteredProducts.filter((p) =>
        p.tags.includes(filters.tag)
      );
    }

    // Filter by featured
    if (filters.featured !== undefined) {
      filteredProducts = filteredProducts.filter(
        (p) => p.featured === filters.featured
      );
    }

    // Search by name
    if (filters.search) {
      const searchTerm = filters.search.toLowerCase();
      filteredProducts = filteredProducts.filter(
        (p) =>
          p.name.toLowerCase().includes(searchTerm) ||
          p.nameEn.toLowerCase().includes(searchTerm) ||
          p.description.toLowerCase().includes(searchTerm)
      );
    }

    // Sort products
    if (filters.sortBy) {
      switch (filters.sortBy) {
        case "price-low":
          filteredProducts.sort((a, b) => a.price - b.price);
          break;
        case "price-high":
          filteredProducts.sort((a, b) => b.price - a.price);
          break;
        case "rating":
          filteredProducts.sort((a, b) => b.rating - a.rating);
          break;
        case "reviews":
          filteredProducts.sort((a, b) => b.reviews - a.reviews);
          break;
        case "newest":
          filteredProducts.sort((a, b) => b.id - a.id);
          break;
        default:
          break;
      }
    }

    // Pagination
    const page = filters.page || 1;
    const limit = filters.limit || 20;
    const startIndex = (page - 1) * limit;
    const endIndex = startIndex + limit;
    const paginatedProducts = filteredProducts.slice(startIndex, endIndex);

    return {
      success: true,
      data: paginatedProducts,
      pagination: {
        page,
        limit,
        total: filteredProducts.length,
        totalPages: Math.ceil(filteredProducts.length / limit),
      },
      message: "Products fetched successfully",
    };
  },

  // Get product by ID
  getProductById: async (id) => {
    await delay(500);
    const product = mockProducts.find((p) => p.id === parseInt(id));
    if (!product) {
      return {
        success: false,
        data: null,
        message: "Product not found",
      };
    }
    return {
      success: true,
      data: product,
      message: "Product fetched successfully",
    };
  },

  // Get featured products
  getFeaturedProducts: async (limit = 4) => {
    await delay(600);
    const featured = mockProducts
      .filter((p) => p.featured)
      .slice(0, limit);
    return {
      success: true,
      data: featured,
      message: "Featured products fetched successfully",
    };
  },

  // Get products by category
  getProductsByCategory: async (categoryId, limit = null) => {
    await delay(600);
    let products = mockProducts.filter(
      (p) => p.categoryId === parseInt(categoryId)
    );
    if (limit) {
      products = products.slice(0, limit);
    }
    return {
      success: true,
      data: products,
      message: "Products fetched successfully",
    };
  },

  // Search products
  searchProducts: async (query) => {
    await delay(700);
    const searchTerm = query.toLowerCase();
    const results = mockProducts.filter(
      (p) =>
        p.name.toLowerCase().includes(searchTerm) ||
        p.nameEn.toLowerCase().includes(searchTerm) ||
        p.description.toLowerCase().includes(searchTerm) ||
        p.category.toLowerCase().includes(searchTerm)
    );
    return {
      success: true,
      data: results,
      message: "Search completed successfully",
    };
  },
};

